const fs = require('fs');

const content = [
  "import { CaixaEletronicoService } from \"../services/CaixaEletronicoService\";",
  "import { RepositorioContas } from \"../models/RepositorioContas\";",
  "import { ConsoleUI } from \"../views/ConsoleUI\";",
  "import { FluxoSaque } from \"../views/FluxoSaque\";",
  "import { FluxoAdmin } from \"../views/FluxoAdmin\";",
  "",
  "/**",
  " * Controlador principal do caixa eletronico (padrao MVC).",
  " * Responsabilidades: autenticacao, menu do usuario, delegacao de fluxos.",
  " */",
  "export class CaixaEletronicoController {",
  "  private readonly ui: ConsoleUI;",
  "  private readonly fluxoSaque: FluxoSaque;",
  "  private readonly fluxoAdmin: FluxoAdmin;",
  "  private contaAtualId: string | null = null;",
  "",
  "  constructor(",
  "    private readonly service: CaixaEletronicoService,",
  "    private readonly repositorioContas: RepositorioContas",
  "  ) {",
  "    this.ui = new ConsoleUI();",
  "    this.fluxoSaque = new FluxoSaque(service, repositorioContas, this.ui);",
  "    this.fluxoAdmin = new FluxoAdmin(service, this.ui);",
  "  }",
  "",
  "  // Inicia o sistema e exibe a tela de autenticacao.",
  "  iniciar(): void {",
  "    this.ui.limparTela();",
  "    this.ui.exibirBanner();",
  "    this.menuAutenticacao();",
  "  }",
  "",
  "  private menuAutenticacao(): void {",
  "    console.log(\"\\n\\u2554\" + \"\\u2550\".repeat(30) + \"\\u2557\");",
  "    console.log(\"\\u2551      AUTENTICACAO              \\u2551\");",
  "    console.log(\"\\u255a\" + \"\\u2550\".repeat(30) + \"\\u255d\");",
  "",
  "    this.ui.perguntar(\"  Digite o ID da conta (ou 'admin'): \").then(id => {",
  "      const idTrimado = id.trim();",
  "",
  "      if (idTrimado.toLowerCase() === \"admin\") {",
  "        this.fluxoAdmin.iniciar(() => this.menuAutenticacao());",
  "        return;",
  "      }",
  "",
  "      const conta = this.repositorioContas.buscarPorId(idTrimado);",
  "      if (!conta) {",
  "        console.log(`\\n  Conta \"${idTrimado}\" nao encontrada.`);",
  "        this.menuAutenticacao();",
  "        return;",
  "      }",
  "",
  "      this.contaAtualId = idTrimado;",
  "      console.log(`\\n  Bem-vindo(a)! Conta: ${conta.id}`);",
  "      this.menuUsuario();",
  "    });",
  "  }",
  "",
  "  private menuUsuario(): void {",
  "    const conta = this.repositorioContas.buscarPorId(this.contaAtualId!)!;",
  "",
  "    console.log(\"\\n\\u2554\" + \"\\u2550\".repeat(38) + \"\\u2557\");",
  "    console.log(`\\u2551  CONTA: ${conta.id.padEnd(30)}\\u2551`);",
  "    console.log(`\\u2551  SALDO: ${conta.saldoFormatado.padEnd(30)}\\u2551`);",
  "    console.log(\"\\u2560\" + \"\\u2550\".repeat(38) + \"\\u2563\");",
  "    console.log(\"\\u2551  [1] Realizar Saque                    \\u2551\");",
  "    console.log(\"\\u2551  [2] Consultar Saldo                   \\u2551\");",
  "    console.log(\"\\u2551  [3] Sair (Encerrar Sessao)            \\u2551\");",
  "    console.log(\"\\u255a\" + \"\\u2550\".repeat(38) + \"\\u255d\");",
  "",
  "    // Mapa de acoes (padrao Command) — elimina switch/case",
  "    const acoes: Record<string, () => void> = {",
  "      \"1\": () => this.fluxoSaque.iniciar(this.contaAtualId!, () => this.menuUsuario()),",
  "      \"2\": () => {",
  "        console.log(`\\n  Saldo atual: ${conta.saldoFormatado}`);",
  "        this.ui.pausar().then(() => this.menuUsuario());",
  "      },",
  "      \"3\": () => {",
  "        this.contaAtualId = null;",
  "        console.log(\"\\n  Sessao encerrada. Ate logo!\\n\");",
  "        this.menuAutenticacao();",
  "      },",
  "    };",
  "",
  "    this.ui.perguntar(\"\\n  Opcao: \").then((opcao: string) => {",
  "      const acao = acoes[opcao.trim()] ?? (() => {",
  "        console.log(\"\\n  Opcao invalida.\");",
  "        this.menuUsuario();",
  "      });",
  "      acao();",
  "    });",
  "  }",
  "",
  "  fechar(): void {",
  "    this.ui.fechar();",
  "  }",
  "}",
].join('\n');

fs.writeFileSync(
  'src/controllers/CaixaEletronicoController.ts',
  content,
  'utf8'
);
console.log('Arquivo gravado com sucesso.');
